package com.zingbytes.maxziapp.model;


public class AllFoodItem {

    private String foodname;
    private String foodCat;
    private String foodSubCat;


    public String getFoodname() {
        return foodname;
    }

    public void setFoodname(String foodname) {
        this.foodname = foodname;
    }

    public String getFoodCat() {
        return foodCat;
    }

    public void setFoodCat(String foodCat) {
        this.foodCat = foodCat;
    }

    public String getFoodSubCat() {
        return foodSubCat;
    }

    public void setFoodSubCat(String foodSubCat) {
        this.foodSubCat = foodSubCat;
    }

}